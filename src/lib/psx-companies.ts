/**
 * Look up the full company name for a PSX stock symbol.
 *
 * @param symbol   - The ticker symbol (e.g. "OGDC", "HBL")
 * @param fallback - Optional fallback string returned when the symbol
 *                   is not found in the map. Defaults to the symbol itself.
 * @returns The full company name, or the fallback, or the symbol itself.
 *
 * @example
 * getCompanyName('OGDC');           // "Oil & Gas Development Company Limited"
 * getCompanyName('HBL');            // "Habib Bank Limited"
 * getCompanyName('XYZ');            // "XYZ"
 * getCompanyName('XYZ', 'Unknown'); // "Unknown"
 */
export function getCompanyName(symbol: string, fallback?: string): string {
  if (!symbol) return fallback ?? '';
  const normalized = symbol.trim().toUpperCase();
  return PSX_COMPANY_NAMES[normalized] ?? fallback ?? normalized;
}
